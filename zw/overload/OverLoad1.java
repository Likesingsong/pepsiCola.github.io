package zw.overload;

public class OverLoad1 {
    public static void main(String[] args) {
        /**
         * 1、方法名必须相同
         * 2、形参列表必须不同（包括形参个数、顺序以及形参的类型）
         * 3、返回类型无要求
         */
        System.out.println(100);
        System.out.println("hello");
        System.out.println('h');
        System.out.println(1.5);
    }
}
