package zw.JavaFile.com;

public class Byte {
    public static void main(String[] args) {
        int i = 21;
        //相当于 21 * 2 * 2 = 84
        System.out.println(" i << 2 = " + (i << 2));
        System.out.println(" i << 2 = " + (i << 26));
        System.out.println(" i << 2 = " + (i << 27));
    }
}
