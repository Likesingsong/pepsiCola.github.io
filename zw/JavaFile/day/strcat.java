package code.JavaFile.day;

public class strcat {
    public static void main(String[] args) {
        int a = 10;
        char c = 'a';
        String str = "hello";
        System.out.println(a + c + str);
        System.out.println(a + str + c);
        System.out.println(c + a + str);
        System.out.println(str + c + a);
    }
}
